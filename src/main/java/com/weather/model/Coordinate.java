package com.weather.model;

public class Coordinate {

	private int lon;
	private int lat;

	public Coordinate() {
	}

	public int getLon() {
		return lon;
	}

	public void setLon(int lon) {
		this.lon = lon;
	}

	public int getLat() {
		return lat;
	}

	public void setLat(int lat) {
		this.lat = lat;
	}

}
